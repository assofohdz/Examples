import com.simsilica.sim.AbstractGameSystem;
import com.simsilica.sim.SimTime;
import infinity.net.server.MultiDataHostedService;


/**
 *  A game system that simply hooks into the game loop to
 *  periodically flush the entity buffers.  This is required
 *  for servers the use EntityHostedService.
 *
 *  @author    Paul Speed
 */
public class MultiEntityUpdater extends AbstractGameSystem {

    MultiDataHostedService entityService;
    
    public MultiEntityUpdater( MultiDataHostedService entityService ) {
        this.entityService = entityService;
    }

    @Override
    protected void initialize() {
    }

    @Override
    public void update( SimTime time ) {
        entityService.sendUpdates();   
    }

    @Override
    protected void terminate() {
    }
}
